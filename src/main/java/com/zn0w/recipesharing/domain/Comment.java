package com.zn0w.recipesharing.domain;

public class Comment {
	
	private String author, content;
	
	public Comment(String author, String content) {
		this.author = author;
		this.content = content;
	}
	
	public String getAuthor() {
		return author;
	}
	
	public String getContent() {
		return content;
	}
	
}
